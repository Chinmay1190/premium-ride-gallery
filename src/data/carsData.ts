export const getAllCars = (): Car[] => {
  return carBrands.flatMap(brand => brand.cars);
};

export const getFeaturedCars = (): Car[] => {
  return getAllCars().filter(car => car.isFeatured);
};

export const getCarsByBrand = (brandId: string): Car[] => {
  const brand = carBrands.find(b => b.id === brandId);
  return brand ? brand.cars : [];
};

export const getCarById = (carId: string): Car | undefined => {
  return getAllCars().find(car => car.id === carId);
};

export const getBrandById = (brandId: string): Brand | undefined => {
  return carBrands.find(brand => brand.id === brandId);
};

export const formatPrice = (price: number): string => {
  return new Intl.NumberFormat('en-IN', {
    style: 'currency',
    currency: 'INR',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(price);
};
